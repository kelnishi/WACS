            "int"    => "PushI32Fast",
            "uint"   => "PushU32Fast",
            "long"   => "PushI64Fast",
            "ulong"  => "PushU64Fast",
            "float"  => "PushF32Fast",
            "double" => "PushF64Fast",
            "Wacs.Core.Runtime.Value" => "PushValueFast",
