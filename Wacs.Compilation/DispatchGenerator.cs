namespace Wacs.Compilation
{
    /// <summary>
    /// Source generator for the monolithic-switch WebAssembly interpreter.
    ///
    /// <para>Each method marked <c>[OpSource]</c> (stack-only numeric op) or <c>[OpHandler]</c>
    /// (any op including those with immediates or control-flow side effects) contributes a
    /// <c>case</c> to the generated <c>GeneratedDispatcher.Run</c> method. Inside that case
    /// we emit — inline, no wrappers:</para>
    /// <list type="number">
    ///   <item>Immediate reads from the annotated bytecode stream (one <c>StreamReader.ReadX</c>
    ///         call per <c>[Imm]</c> parameter, in declaration order; advances <c>pc</c>).</item>
    ///   <item>Stack pops into named locals matching the parameter names (reverse param order,
    ///         direct <c>ctx.OpStack.PopX()</c> calls — the handler body can reference them
    ///         by the same names it declared).</item>
    ///   <item>The handler body, with <c>return &lt;expr&gt;;</c> rewritten to <c>{ __r = &lt;expr&gt;;
    ///         goto __end_N; }</c> for non-void handlers, and <c>return;</c> rewritten to
    ///         <c>goto __end_N;</c> for void handlers.</item>
    ///   <item>A per-case label <c>__end_N:</c> followed by the result push (non-void only)
    ///         and <c>continue;</c> to return to the dispatch loop.</item>
    /// </list>
    /// <para>Rationale: every layer of function call — even an inlined <c>static __Op(...)</c>
    /// local function — demands a call frame in the IL and is IL the polymorphic dispatcher
    /// doesn't emit. The whole point of source-generating this dispatcher is to emit ONLY
    /// the instruction's semantics and nothing else.</para>
    ///
    /// <para>Dispatch structure: a single <c>while (pc &lt; code.Length)</c> loop at the top of
    /// <c>Run</c>, one <c>switch</c> on the primary byte, primary-only opcodes inlined in
    /// that switch. The five prefix opcodes (<c>0xFB..0xFF</c>) delegate to per-prefix
    /// sub-methods (<c>DispatchFB</c>, <c>DispatchFC</c>, ...) — they handle a single op and
    /// return. The benchmark-hot primaries stay in a single frame; SIMD/GC/atomics pay a
    /// call per op, which is tolerable since they aren't the hot path.</para>
    ///
    /// <para>AOT-safe: all introspection (attribute lookup, parameter classification) happens
    /// in the generator at build time. The emitted code uses no reflection, emit, or late
    /// binding. Roslyn lowers it to plain IL like any other C#.</para>
    /// </summary>
    [Generator]
    public class DispatchGenerator : IIncrementalGenerator
    {
        private const string OpSourceAttrFullName  = "Wacs.Core.Compilation.OpSourceAttribute";
        private const string OpHandlerAttrFullName = "Wacs.Core.Compilation.OpHandlerAttribute";
        private const string ImmAttrFullName       = "Wacs.Core.Compilation.ImmAttribute";

        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            var entries = context.SyntaxProvider
                .CreateSyntaxProvider(
                    predicate: static (node, _) => IsCandidateMethod(node),
                    transform: static (ctx, ct) => Extract(ctx, ct))
                .Where(static e => e != null)
                .Collect();

            context.RegisterSourceOutput(entries, (spc, items) =>
            {
                try
                {
                    Emit(spc, items);
                }
                catch (System.Exception ex)
                {
                    spc.AddSource("GeneratedDispatcher.Error.g.cs",
                        SourceText.From(
                            "// <auto-generated/>\n// DispatchGenerator threw:\n// " +
                            ex.GetType().FullName + ": " + ex.Message.Replace("\n", " ") + "\n" +
                            "// Stack:\n// " + (ex.StackTrace ?? "").Replace("\n", "\n// ") + "\n",
                            Encoding.UTF8));
                }
            });
        }

        private static bool IsCandidateMethod(SyntaxNode node)
        {
            if (node is not MethodDeclarationSyntax m) return false;
            if (m.AttributeLists.Count == 0) return false;
            foreach (var list in m.AttributeLists)
                foreach (var attr in list.Attributes)
                {
                    var name = attr.Name.ToString();
                    if (name.Contains("OpSource") || name.Contains("OpHandler"))
                        return true;
                }
            return false;
        }

        private static DispatchEntry Extract(GeneratorSyntaxContext ctx, CancellationToken ct)
        {
            var method = (MethodDeclarationSyntax)ctx.Node;
            var symbol = ctx.SemanticModel.GetDeclaredSymbol(method, ct) as IMethodSymbol;
            if (symbol == null) return null;

            AttributeData opAttr = null;
            bool isHandler = false;
            foreach (var a in symbol.GetAttributes())
            {
                var full = a.AttributeClass?.ToDisplayString();
                if (full == OpSourceAttrFullName)  { opAttr = a; isHandler = false; break; }
                if (full == OpHandlerAttrFullName) { opAttr = a; isHandler = true;  break; }
            }
            if (opAttr == null) return null;
            if (opAttr.ConstructorArguments.Length != 1) return null;

            var arg = opAttr.ConstructorArguments[0];
            if (arg.Kind != TypedConstantKind.Enum || arg.Type == null || arg.Value == null) return null;

            int rawValue = System.Convert.ToInt32(arg.Value);
            string enumTypeName = arg.Type.Name;
            string enumName = arg.Type.ToDisplayString();
            string enumMember = arg.Type.GetMembers()
                .OfType<IFieldSymbol>()
                .FirstOrDefault(f => f.HasConstantValue && System.Convert.ToInt32(f.ConstantValue) == rawValue)?.Name
                ?? rawValue.ToString();

            var parameters = ImmutableArray.CreateBuilder<ParamInfo>(symbol.Parameters.Length);
            foreach (var p in symbol.Parameters)
            {
                parameters.Add(ClassifyParam(p, isHandler));
            }

            string returnType = symbol.ReturnType.SpecialType == SpecialType.System_Void
                ? "void"
                : symbol.ReturnType.ToDisplayString();

            // Body text: for expression-bodied methods we keep just the expression; for
            // block bodies we keep the raw { ... } text. Both go through the same return-
            // rewriter at emit time.
            string bodyText;
            bool bodyIsExpression;
            if (method.ExpressionBody != null)
            {
                bodyText = method.ExpressionBody.Expression.ToFullString().Trim();
                bodyIsExpression = true;
            }
            else if (method.Body != null)
            {
                bodyText = method.Body.ToFullString();
                bodyIsExpression = false;
            }
            else
            {
                return null;
            }

            return new DispatchEntry(enumTypeName, rawValue, symbol.Name, enumName, enumMember,
                                     parameters.ToImmutable(), returnType, bodyText, bodyIsExpression,
                                     isHandler);
        }

        private static ParamInfo ClassifyParam(IParameterSymbol p, bool isHandler)
        {
            string type = p.Type.ToDisplayString();
            if (isHandler && p.RefKind == RefKind.Ref && p.Type.SpecialType == SpecialType.System_Int32 && p.Name == "pc")
                return new ParamInfo(p.Name, type, ParamKind.RefPc);
            if (isHandler && type == "Wacs.Core.Runtime.ExecContext")
                return new ParamInfo(p.Name, type, ParamKind.Ctx);
            if (isHandler && type == "System.ReadOnlySpan<byte>")
                return new ParamInfo(p.Name, type, ParamKind.Code);
            if (isHandler)
            {
                foreach (var a in p.GetAttributes())
                {
                    if (a.AttributeClass?.ToDisplayString() == ImmAttrFullName)
                        return new ParamInfo(p.Name, type, ParamKind.Immediate);
                }
            }
            return new ParamInfo(p.Name, type, ParamKind.Stack);
        }

        private static void Emit(SourceProductionContext spc, ImmutableArray<DispatchEntry> entries)
        {
            // Dedup by (enum, value) — if a handler is declared twice with the same key,
            // keep the first. Primary ops and prefix ops can collide on rawValue but not
            // on enum type, so the dedup key is the pair.
            var deduped = new Dictionary<string, DispatchEntry>();
            foreach (var e in entries)
            {
                if (e == null) continue;
                var key = e.EnumTypeName + ":" + e.RawValue;
                if (!deduped.ContainsKey(key)) deduped[key] = e;
            }
            var ordered = deduped.Values.ToList();

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("#nullable disable");
            // CS0164: label defined but never used (bodies without `return` leave their __end_N label unused).
            // CS0162: unreachable code (bodies ending with `throw` leave the trailing goto unreachable).
            sb.AppendLine("#pragma warning disable 164, 162");
            sb.AppendLine("using System;");
            sb.AppendLine("using System.Buffers.Binary;");
            sb.AppendLine("using System.Collections.Generic;");
            sb.AppendLine("using System.Runtime.InteropServices;");
            sb.AppendLine("using Wacs.Core.OpCodes;");
            sb.AppendLine("using Wacs.Core.Runtime;");
            sb.AppendLine("using Wacs.Core.Runtime.Exceptions;");
            sb.AppendLine("using Wacs.Core.Runtime.GC;");
            sb.AppendLine("using Wacs.Core.Runtime.Types;");
            sb.AppendLine("using Wacs.Core.Types;");
            sb.AppendLine("using Wacs.Core.Types.Defs;");
            sb.AppendLine("using Wacs.Core.Instructions;");
            sb.AppendLine("using Wacs.Core.Instructions.SIMD;");
            sb.AppendLine("using Wacs.Core.Instructions.GC;");
            sb.AppendLine("using Wacs.Core.Utilities;");
            sb.AppendLine();
            sb.AppendLine("namespace Wacs.Core.Compilation");
            sb.AppendLine("{");
            sb.AppendLine("    public static partial class GeneratedDispatcher");
            sb.AppendLine("    {");
            // Mirror bit-manipulation constants that live as `private const` inside the
            // instruction classes. Bodies copied verbatim into this partial class reference
            // these by unqualified name — this block keeps the names resolvable. Values are
            // identical to the originals; update in lockstep if ever changed.
            sb.AppendLine("        // Mirrored from InstF32BinOp");
            sb.AppendLine("        private const uint F32SignMask = 0x8000_0000u;");
            sb.AppendLine("        private const uint F32NotSignMask = ~F32SignMask;");
            sb.AppendLine("        // Mirrored from InstF64BinOp");
            sb.AppendLine("        private const ulong F64SignMask = 0x8000_0000_0000_0000ul;");
            sb.AppendLine("        private const ulong F64NotSignMask = ~F64SignMask;");
            sb.AppendLine("        // Mirrored from InstI32SignExtension");
            sb.AppendLine("        private const uint ByteSign = 0x80u;");
            sb.AppendLine("        private const uint I32ByteExtend = 0xFFFF_FF80u;");
            sb.AppendLine("        private const uint ByteMask = 0xFFu;");
            sb.AppendLine("        private const uint ShortSign = 0x8000u;");
            sb.AppendLine("        private const uint I32ShortExtend = 0xFFFF_8000u;");
            sb.AppendLine("        private const uint ShortMask = 0xFFFFu;");
            sb.AppendLine("        // Mirrored from InstI64SignExtension");
            sb.AppendLine("        private const ulong I64ByteExtend = 0xFFFF_FFFF_FFFF_FF80ul;");
            sb.AppendLine("        private const ulong I64ShortExtend = 0xFFFF_FFFF_FFFF_8000ul;");
            sb.AppendLine("        private const ulong WordSign = 0x8000_0000ul;");
            sb.AppendLine("        private const ulong WordExtend = 0xFFFF_FFFF_8000_0000ul;");
            sb.AppendLine("        private const ulong WordMask = 0xFFFF_FFFFul;");
            sb.AppendLine();

            // Group entries by prefix. Primary ops (OpCode enum) inline into Run; the rest
            // go to a per-prefix sub-method.
            var primaryOps = ordered.Where(e => e.EnumTypeName == "OpCode").ToList();
            var prefixGroups = new[]
            {
                ("GcCode",   (byte)0xFB),
                ("ExtCode",  (byte)0xFC),
                ("SimdCode", (byte)0xFD),
                ("AtomCode", (byte)0xFE),
                ("WacsCode", (byte)0xFF),
            };

            // Emit Run — the top-level dispatch method. While-loop + primary-byte switch +
            // prefix cases that delegate to sub-methods. pcBefore is stored on ExecContext
            // and updated each iteration so the exception-handler range check in
            // SwitchRuntime can reconstruct the pc of the throwing op (pc itself has
            // already advanced past the op's bytes by the time the throw propagates).
            sb.AppendLine("        /// <summary>");
            sb.AppendLine("        /// Dispatch loop. Reads the primary byte, switches on it, and runs the inlined");
            sb.AppendLine("        /// body of each opcode to completion before looping. Prefix opcodes (0xFB-0xFF)");
            sb.AppendLine("        /// delegate to per-prefix sub-methods that handle their secondary byte(s).");
            sb.AppendLine("        /// </summary>");
            // SkipLocalsInit elides the method's .locals init flag, so the JIT doesn't emit
            // the frame-zeroing loop at entry. Run has a ~1.6 KiB frame (many per-case locals),
            // so the default dczva-based zero-fill is several hundred bytes of prologue. Safe
            // here because every local is explicitly assigned before any read — the generator
            // emits `{T} name = ...;` on first use in each case, and the method-level hoists
            // (_pc, _pcBefore, _opStack, etc.) are all initialized immediately.
            sb.AppendLine("        [System.Runtime.CompilerServices.SkipLocalsInit]");
            sb.AppendLine("        public static void Run(ExecContext ctx, ReadOnlySpan<byte> code)");
            sb.AppendLine("        {");
            // Hoist the OpStack and the current frame's locals out of the hot loop. OpStack
            // is a readonly field (bound at ExecContext construction), so it's trivially safe.
            // Frame.Locals is fixed for the duration of THIS Run invocation — Call opcodes
            // recurse into nested InvokeWasm which saves+restores ctx.Frame around the inner
            // Run, so our hoisted span is never observed stale from within our own switch.
            sb.AppendLine("            var _opStack = ctx.OpStack;");
            sb.AppendLine("            var _localsSpan = ctx.Frame.Locals.Span;");
            // Hoist a raw ref to _registers[0]. The emit-time-inlined pops/pushes below
            // index off this ref, bypassing both the safe array indexer (bounds check)
            // and the Pop*Fast method-call boundary — RyuJIT declines to inline the Fast
            // wrappers because Run exceeds its inlining budget, so each call would stay
            // as an out-of-line blr. Inlining here reduces every pop/push to a single
            // Unsafe.Add + .Data.<field> access with zero calls.
            sb.AppendLine("            ref Wacs.Core.Runtime.Value _registersRef = ref _opStack.FirstRegister();");
            // pc / pcBefore are plain method-local ints, seeded from ctx fields. Previously
            // these were `ref int` parameters; the ref aliased the locals back to a caller
            // stack slot, which RyuJIT treated conservatively and spilled _pc to memory on
            // every modification (visible as 3× ldr+str pairs around each immediate-read in
            // the hot dispatch). Passing state via ctx fields instead lets _pc live in a
            // register across the loop body. Writeback at loop exit (in the finally) keeps
            // SwitchRuntime's handler-resume path observing current values even on throws.
            sb.AppendLine("            int _pc = ctx.SwitchPc;");
            sb.AppendLine("            int _pcBefore = ctx.SwitchPcBefore;");
            // Hoist the span's raw byte reference + length so the per-iteration fetch can
            // use Unsafe.Add (no bounds check) — the while-loop guard already ensures
            // _pc &lt; _codeLen at the read site. GetReference is an intrinsic that lowers
            // to a single pointer read; stays AOT-safe.
            sb.AppendLine("            ref byte _codeBase = ref System.Runtime.InteropServices.MemoryMarshal.GetReference(code);");
            sb.AppendLine("            int _codeLen = code.Length;");
            // try/finally restored. Correctness beats micro-perf here: SwitchRuntime's
            // handler-resume path reads pc/pcBefore via the refs after a WasmException
            // escapes, and if we don't write them back on exceptional exit the caller
            // sees their pre-call (zero) state and the range check against handler
            // StartPc/EndPc fails. Removing the try/finally didn't actually get _pc
            // into a register anyway — the ref parameter is what pins it, not the
            // exception region. That's a separate fix (phase F candidate).
            sb.AppendLine("            try");
            sb.AppendLine("            {");
            sb.AppendLine("                while (_pc < _codeLen)");
            sb.AppendLine("                {");
            sb.AppendLine("                    _pcBefore = _pc;");
            sb.AppendLine("                    byte primary = System.Runtime.CompilerServices.Unsafe.Add(ref _codeBase, _pc);");
            sb.AppendLine("                    _pc++;");
            sb.AppendLine("                    switch (primary)");
            sb.AppendLine("                    {");
            int caseCounter = 0;
            // Hot/cold primary-opcode split. HOT ops — control flow, locals/globals,
            // parametric, consts, i32/i64/f32/f64 arith + cmp + essential load/store —
            // stay inline in Run's switch so the hot loop is a direct jump-table dispatch
            // into the opcode body. COLD ops — div/rem, rotl/rotr, bit-counting,
            // saturating/float conversions, sign-extension, ref types, exceptions,
            // memory size/grow — route through a single Run default case into the
            // DispatchCold sub-method. Rationale: the OpProfile survey found the hot
            // set covers >95% of execution across coremark / wasm2wat / perl / f64-numeric
            // while shrinking Run's jump table enough that the JIT can optimise it.
            var hotPrimaries = HotPrimaryOpcodes();
            var hotPrimaryOps = primaryOps.Where(e => hotPrimaries.Contains(e.RawValue & 0xFF)).ToList();
            var coldPrimaryOps = primaryOps.Where(e => !hotPrimaries.Contains(e.RawValue & 0xFF)).ToList();

            foreach (var e in hotPrimaryOps.OrderBy(e => e.RawValue))
            {
                EmitCase(sb, e, "                        ", ref caseCounter, singleByteKey: true, inLoop: true, pcVar: "_pc", inlineStack: true);
            }
            // Sub-methods return the new pc as an int, not via a ref parameter. Any
            // `ref int pc` in a sub-method signature would force Run's _pc to be
            // address-taken (has to live in the stack frame), which defeats the whole
            // point of the ExecContext-fields refactor. int return + reassign keeps
            // _pc register-resident across the hot loop.
            foreach (var (_, pb) in prefixGroups)
            {
                sb.AppendLine($"                        case 0x{pb:X2}: _pc = Dispatch{pb:X2}(ctx, code, _pc); continue;");
            }
            sb.AppendLine("                        default: _pc = DispatchCold(ctx, code, _pc, primary); continue;");
            sb.AppendLine("                    }");
            sb.AppendLine("                }");
            sb.AppendLine("            }");
            sb.AppendLine("            finally");
            sb.AppendLine("            {");
            sb.AppendLine("                ctx.SwitchPc = _pc;");
            sb.AppendLine("                ctx.SwitchPcBefore = _pcBefore;");
            sb.AppendLine("            }");
            sb.AppendLine("        }");
            sb.AppendLine();

            // DispatchCold: the long-tail primary-opcode sub-method. Same per-case
            // body shape as Run's inline hot cases (emits through EmitCase), but each
            // cold invocation takes a function-call hit: one blr into this method,
            // one re-hoist of _opStack / _localsSpan / _codeBase, one body, one ret.
            // The 5 hottest workloads in OpProfile put the cold-set fraction at
            // 0–11% of execution, so the amortised regression is small.
            EmitColdDispatch(sb, coldPrimaryOps, ref caseCounter);

            // Per-prefix sub-methods. Each reads its secondary byte (or u16 for SIMD) and
            // dispatches a single op. Returns control to Run's loop after.
            foreach (var (enumType, prefixByte) in prefixGroups)
            {
                var subEntries = ordered.Where(e => e.EnumTypeName == enumType).OrderBy(e => e.RawValue).ToList();
                EmitPrefixSubMethod(sb, enumType, prefixByte, subEntries, ref caseCounter);
            }

            sb.AppendLine();
            sb.AppendLine("        /// <summary>The number of ops the generator emitted cases for.</summary>");
            sb.AppendLine($"        public const int HandledOpcodeCount = {ordered.Count};");
            sb.AppendLine("    }");
            sb.AppendLine("}");

            spc.AddSource("GeneratedDispatcher.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
        }

        /// <summary>
        /// Emit <c>DispatchCold</c> — sub-method covering cold primary opcodes. Receives
        /// the primary byte from Run (already-read), hoists its own copies of the stream
        /// locals, and dispatches on the primary to the right case body. Body shape per
        /// case is identical to Run's inline hot cases (same <see cref="EmitCase"/> code
        /// path). Returns to Run which continues the loop.
        /// </summary>
        private static void EmitColdDispatch(StringBuilder sb, List<DispatchEntry> coldEntries, ref int caseCounter)
        {
            sb.AppendLine("        private static int DispatchCold(ExecContext ctx, ReadOnlySpan<byte> code, int pc, byte primary)");
            sb.AppendLine("        {");
            // Mirror Run's hoisted locals so EmitCase's output can reference _opStack /
            // _localsSpan / _codeBase unchanged. One re-hoist per cold-op execution — a
            // few mov/ldr instructions, dwarfed by the body of any non-trivial cold op.
            // pc is an int value (not a ref) so Run can keep its local _pc in a
            // register; we return the updated pc for Run to reassign.
            sb.AppendLine("            var _opStack = ctx.OpStack;");
            sb.AppendLine("            var _localsSpan = ctx.Frame.Locals.Span;");
            // No _registersRef hoist here — cold sub-methods stay on the Pop*Fast /
            // Push*Fast method calls. Inlining each case's stack ops would grow the
            // frame enough to overflow the stack on deeply recursive wasm (the
            // call_indirect.wast suite hits ~48 recursive InvokeWasm levels, each
            // stacking a GeneratedDispatcher.Run + DispatchCold frame pair).
            sb.AppendLine("            ref byte _codeBase = ref System.Runtime.InteropServices.MemoryMarshal.GetReference(code);");
            sb.AppendLine("            switch (primary)");
            sb.AppendLine("            {");
            foreach (var e in coldEntries.OrderBy(e => e.RawValue))
            {
                EmitCase(sb, e, "                ", ref caseCounter, singleByteKey: true, inLoop: false, pcVar: "pc", inlineStack: false);
            }
            sb.AppendLine("                default:");
            sb.AppendLine("                    throw new NotSupportedException(");
            sb.AppendLine("                        $\"Opcode 0x{primary:X2} at pc={pc - 1} has no [OpSource]/[OpHandler] coverage.\");");
            sb.AppendLine("            }");
            sb.AppendLine("        }");
            sb.AppendLine();
        }

        /// <summary>
        /// The set of primary-byte opcodes kept inline in <c>Run</c>'s switch. Everything
        /// outside this set is routed to <see cref="EmitColdDispatch"/>.
        ///
        /// <para>Chosen from the OpProfile survey (coremark / wasm2wat / perl / f64-numeric)
        /// and padded out to full op-family symmetry so a workload that shows up at 0% in
        /// our samples but uses the family in a production setting (i64 in 64-bit targets,
        /// f32 in graphics, extensions for iterating over smaller types) still hits the
        /// hot path.</para>
        ///
        /// <para>What's NOT here (and therefore demoted to cold): div/rem (trap-heavy),
        /// rotl/rotr, integer truncating float conversions (trap-heavy), saturating
        /// conversions, reference-type ops, exception ops (throw/throw_ref/try_table),
        /// memory.size/grow, table ops. Demoted ops pay one sub-method-call hit per
        /// execution — acceptable since none appeared above 1% in any profiled workload.</para>
        /// </summary>
        private static HashSet<int> HotPrimaryOpcodes()
        {
            return new HashSet<int>
            {
                // Control — keep direct call (0x10) and call_indirect (0x11) hot since
                // non-trivial programs hit them constantly. Tail-call / call-ref variants
                // (0x12-0x15) have heavier bodies (frame setup, type checks) and appear
                // rarely — demote them to the cold sub-method.
                0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
                0x10, 0x11,
                // Parametric
                0x1A, 0x1B, 0x1C,
                // Variable (local + global)
                0x20, 0x21, 0x22, 0x23, 0x24,
                // Loads: i32/i64/f32/f64 full-width + i32 narrow widths (used heavily in
                // string-handling code — wasm2wat had 2% i32.load8_u, 1.3% i32.load16_u).
                // i64 narrow loads (0x30-0x35) left cold — they share the bounds-check
                // and alignment machinery with the full-width loads but aren't seen in
                // any profiled workload.
                0x28, 0x29, 0x2A, 0x2B, 0x2C, 0x2D, 0x2E, 0x2F,
                // Stores: same principle — core stores hot, i64 narrow stores cold.
                0x36, 0x37, 0x38, 0x39, 0x3A, 0x3B,
                // Consts (tiny bodies)
                0x41, 0x42, 0x43, 0x44,
                // i32 relational + eqz (0x45..0x4F)
                0x45, 0x46, 0x47, 0x48, 0x49, 0x4A, 0x4B, 0x4C, 0x4D, 0x4E, 0x4F,
                // i64 relational + eqz (0x50..0x5A)
                0x50, 0x51, 0x52, 0x53, 0x54, 0x55, 0x56, 0x57, 0x58, 0x59, 0x5A,
                // f32 relational (0x5B..0x60)
                0x5B, 0x5C, 0x5D, 0x5E, 0x5F, 0x60,
                // f64 relational (0x61..0x66)
                0x61, 0x62, 0x63, 0x64, 0x65, 0x66,
                // i32 arith: add, sub, mul, and, or, xor, shl, shr_s, shr_u. Clz/ctz/
                // popcnt and div/rem stay cold (rarer, div/rem has trap paths).
                0x6A, 0x6B, 0x6C, 0x71, 0x72, 0x73, 0x74, 0x75, 0x76,
                // i64 arith: same family
                0x7C, 0x7D, 0x7E, 0x83, 0x84, 0x85, 0x86, 0x87, 0x88,
                // f32 arith: add, sub, mul, div (tiny single-op bodies). Unary f32
                // (abs/neg/sqrt/ceil/floor/trunc/nearest) and min/max/copysign stay
                // cold — they're rarer in real code and add bytes to Run.
                0x92, 0x93, 0x94, 0x95,
                // f64 arith: add, sub, mul, div
                0xA0, 0xA1, 0xA2, 0xA3,
                // i64.extend_i32_s (0xAC) — a single (long)i cast. Appears in the
                // inner loop of any i32-counter → i64-accumulator pattern (sum(5M)
                // hits it 5M times, fac 5M). Cold demotion would cost a full
                // DispatchCold round-trip per iteration.
                0xAC,
            };
        }

        /// <summary>
        /// Emit a per-prefix sub-dispatcher. Reads the secondary byte (single byte for all
        /// prefixes except SIMD, which uses a u16 to accommodate relaxed-SIMD opcodes >=
        /// 0x100), switches on it, and runs one op to completion before returning.
        /// </summary>
        private static void EmitPrefixSubMethod(StringBuilder sb, string enumType, byte prefixByte,
                                                 List<DispatchEntry> entries, ref int caseCounter)
        {
            bool isSimd = enumType == "SimdCode";
            string keyType = isSimd ? "ushort" : "byte";
            sb.AppendLine($"        private static int Dispatch{prefixByte:X2}(ExecContext ctx, ReadOnlySpan<byte> code, int pc)");
            sb.AppendLine("        {");
            // Hoist the same locals Run hoists, so emitted case bodies (_opStack.XXX,
            // _localsSpan[idx], Unsafe.ReadUnaligned(ref _codeBase, ...)) compile uniformly
            // here too. The hoist cost is tiny in a per-op sub-method — a few field reads —
            // and keeps the generator's per-case emit identical between Run and each
            // Dispatch<prefix>.
            sb.AppendLine("            var _opStack = ctx.OpStack;");
            sb.AppendLine("            var _localsSpan = ctx.Frame.Locals.Span;");
            // Prefix sub-methods also stay on method calls (see DispatchCold comment).
            sb.AppendLine("            ref byte _codeBase = ref System.Runtime.InteropServices.MemoryMarshal.GetReference(code);");
            if (isSimd)
            {
                // Two-byte secondary (relaxed-SIMD opcodes go past 0xFF). Unsafe.ReadUnaligned
                // avoids the Slice bounds check that BinaryPrimitives would demand.
                sb.AppendLine("            ushort sec = System.Runtime.CompilerServices.Unsafe.ReadUnaligned<ushort>(ref System.Runtime.CompilerServices.Unsafe.Add(ref _codeBase, pc));");
                sb.AppendLine("            pc += 2;");
            }
            else
            {
                sb.AppendLine("            byte sec = System.Runtime.CompilerServices.Unsafe.Add(ref _codeBase, pc); pc += 1;");
            }
            sb.AppendLine("            switch (sec)");
            sb.AppendLine("            {");
            foreach (var e in entries)
            {
                EmitCase(sb, e, "                ", ref caseCounter, singleByteKey: false, inLoop: false, pcVar: "pc", inlineStack: false);
            }
            sb.AppendLine("                default:");
            sb.AppendLine($"                    throw new NotSupportedException(");
            sb.AppendLine($"                        $\"Prefix 0x{prefixByte:X2} secondary 0x{{sec:X}} has no [OpSource]/[OpHandler] coverage.\");");
            sb.AppendLine("            }");
            sb.AppendLine("        }");
            sb.AppendLine();
        }

        /// <summary>
        /// Emit one case: reads immediates, pops stack operands into named locals, inlines
        /// the handler body with returns rewritten to the per-case push+continue pattern.
        /// No wrappers, no static local functions — each case is raw inlined logic.
        /// </summary>
        /// <param name="singleByteKey">True when this case lives in <c>Run</c>'s primary
        /// switch (key is the primary byte, same as the opcode value for OpCode-enum ops).
        /// False when inside a prefix sub-method (key is the secondary byte/u16).</param>
        /// <param name="inLoop">True when the switch is inside <c>Run</c>'s while loop — we
        /// end each case with <c>continue;</c>. False inside a prefix sub-method — we end
        /// with <c>return;</c> to hand control back to the caller's loop.</param>
        /// <param name="pcVar">Name of the pc variable in this enclosing scope — <c>"_pc"</c>
        /// in <c>Run</c> (where pc is hoisted from the ref param to a local), <c>"pc"</c> in
        /// prefix sub-methods (where pc is still the ref parameter). Used both for emitted
        /// <c>StreamReader.ReadX(code, ref {pcVar})</c> immediate reads and to textually
        /// rewrite <c>pc</c> identifiers inside handler bodies that take a <c>ref int pc</c>.</param>
        /// <param name="inlineStack">True to emit inline pop/push sequences against the
        /// hoisted <c>_registersRef</c>. False to call the OpStack's <c>Pop*Fast /
        /// Push*Fast</c> methods. The inline form is faster per site, but declares more
        /// temporaries, which grows the method frame. We keep inline on in Run (where the
        /// hot loop runs) and off in cold sub-methods (DispatchCold, Dispatch<prefix>)
        /// because deep recursive-call wasm (e.g. call_indirect.wast) blows the stack
        /// when every frame in the recursion is inline-sized.</param>
        private static void EmitCase(StringBuilder sb, DispatchEntry e, string indent,
                                      ref int caseCounter, bool singleByteKey, bool inLoop,
                                      string pcVar, bool inlineStack)
        {
            int caseId = caseCounter++;
            // Key = raw enum value masked to keyType width. For primary ops (OpCode enum),
            // values already fit in a byte; for prefix ops, SimdCode values can exceed a byte
            // (relaxed-SIMD is 0x100+), so mask accordingly.
            string keyHex = singleByteKey
                ? $"0x{(e.RawValue & 0xFF):X2}"
                : (e.EnumTypeName == "SimdCode"
                    ? $"0x{(e.RawValue & 0xFFFF):X}"
                    : $"0x{(e.RawValue & 0xFF):X2}");

            sb.Append(indent).AppendLine($"case {keyHex}: // {e.EnumName}.{e.EnumMember} — {e.MethodName}");
            sb.Append(indent).AppendLine("{");

            string inner = indent + "    ";

            // 0. Alias the outer Run's ctx/code/pc when the handler's declared parameter
            //    uses a different name (e.g. handlers that named their ExecContext param
            //    `context`). Aliasing lets us inline the body verbatim without textual
            //    parameter renaming. Reference-type aliases (ctx) and span-struct
            //    aliases (code) just copy the 64-bit / 16-byte value; a ref alias for
            //    pc is a single ldloca in IL. No observable overhead after inlining.
            foreach (var p in e.Parameters)
            {
                switch (p.Kind)
                {
                    case ParamKind.Ctx when p.Name != "ctx":
                        sb.Append(inner).AppendLine($"ExecContext {p.Name} = ctx;");
                        break;
                    case ParamKind.Code when p.Name != "code":
                        sb.Append(inner).AppendLine($"System.ReadOnlySpan<byte> {p.Name} = code;");
                        break;
                    // RefPc is not aliased — we textually rewrite the body's `pc` → pcVar
                    // (done in RewriteCtxAccess below). A ref alias would force the JIT to
                    // treat writes through the alias as going through memory, defeating the
                    // register-hoist of _pc in Run.
                }
            }

            // 1. Read immediates in declaration order. Emit inline Unsafe.ReadUnaligned<T>
            //    off the hoisted ref-byte _codeBase instead of calling StreamReader.ReadX —
            //    Run is too large for the JIT to honour StreamReader's [AggressiveInlining]
            //    attribute, so the call would otherwise turn into an out-of-line PLT-style
            //    indirect dispatch per immediate (~11 instructions + a blr). With the
            //    inline form each immediate reduces to one unaligned load + a pc increment.
            //
            //    Little-endian only: .NET supported platforms (x64, ARM64) are all LE, and
            //    the annotated bytecode stream is always emitted LE by BytecodeCompiler. A
            //    big-endian host would need a byte-swap here, but that's a non-goal.
            foreach (var p in e.Parameters.Where(p => p.Kind == ParamKind.Immediate))
            {
                int width = ImmWidth(p.Type);
                if (width == 0)
                {
                    sb.Append(inner).AppendLine($"// skipped — unsupported [Imm] type {p.Type}");
                    sb.Append(inner).AppendLine("throw new NotSupportedException(\"Unsupported immediate type\");");
                    sb.Append(indent).AppendLine("}");
                    return;
                }
                if (p.Type == "byte")
                {
                    // Single-byte read: one Unsafe.Add + deref, no need for ReadUnaligned.
                    sb.Append(inner).AppendLine(
                        $"{p.Type} {p.Name} = System.Runtime.CompilerServices.Unsafe.Add(ref _codeBase, {pcVar}); {pcVar} += 1;");
                }
                else
                {
                    sb.Append(inner).AppendLine(
                        $"{p.Type} {p.Name} = System.Runtime.CompilerServices.Unsafe.ReadUnaligned<{p.Type}>(" +
                        $"ref System.Runtime.CompilerServices.Unsafe.Add(ref _codeBase, {pcVar})); {pcVar} += {width};");
                }
            }

            // 2. Pop stack operands in REVERSE parameter order (WASM semantics: last param
            //    was pushed last, so it's the top of stack). Each pop lands in a local
            //    named after the parameter — the handler body references these by name
            //    verbatim, no renaming needed.
            var stackParams = e.Parameters.Where(p => p.Kind == ParamKind.Stack).ToList();
            for (int i = stackParams.Count - 1; i >= 0; i--)
            {
                var p = stackParams[i];
                if (!EmitInlinePop(sb, inner, p.Type, p.Name, inlineStack))
                {
                    sb.Append(inner).AppendLine($"// skipped — unsupported stack parameter type {p.Type}");
                    sb.Append(inner).AppendLine("throw new NotSupportedException(\"Unsupported stack type\");");
                    sb.Append(indent).AppendLine("}");
                    return;
                }
            }

            // Nothing below this line depends on TypeToPop/Push — pops are already emitted
            // inline above, and pushes below go through EmitInlinePush.

            // 3. Inline the body. Fast path for the overwhelmingly common case
            //    (expression-bodied handler, i.e. `=> expr;`): emit a single push-continue
            //    with the raw expression — no __r temp, no goto, no label. Matches the
            //    MinimalDispatcher shape byte-for-byte.
            //
            //    For block bodies (multi-return, trap paths): declare a result local,
            //    rewrite `return <expr>;` to `{ __r = <expr>; goto __end_N; }`, rewrite
            //    `return;` (void) to `goto __end_N;`, converge at __end_N.
            string endLabel = $"__end_{caseId}";
            if (e.ReturnType != "void" && !IsSupportedPushType(e.ReturnType))
            {
                sb.Append(inner).AppendLine($"// skipped — unsupported return type {e.ReturnType}");
                sb.Append(inner).AppendLine("throw new NotSupportedException(\"Unsupported return type\");");
                sb.Append(indent).AppendLine("}");
                return;
            }

            // inLoop=true → we're in Run's while-loop, fall back to the loop head via
            // `continue`. inLoop=false → we're in a sub-method that returns the updated
            // pc to Run; emit `return pc;` so the value propagates back. Both paths are
            // by-design non-ref — passing pc as a value everywhere is what lets Run
            // keep its `_pc` register-resident across the hot dispatch loop.
            string terminator = inLoop ? "continue;" : $"return {pcVar};";

            if (e.BodyIsExpression)
            {
                string exprBody = RewriteCtxAccess(e.Body, pcVar, inlineStack);
                if (e.ReturnType == "void")
                {
                    sb.Append(inner).AppendLine($"{exprBody}; {terminator}");
                }
                else
                {
                    sb.Append(inner).AppendLine($"{InlinePushStatement(e.ReturnType, exprBody, inlineStack)} {terminator}");
                }
                // No label emitted for expression bodies — nothing jumps to it.
                sb.Append(indent).AppendLine("}");
                return;
            }

            // Block body: needs the result-capture + label pattern for multi-return safety.
            if (e.ReturnType != "void")
                sb.Append(inner).AppendLine($"{e.ReturnType} __r = default;");

            string rewritten = RewriteBody(e.Body, e.BodyIsExpression, e.ReturnType, endLabel, pcVar, inlineStack);
            foreach (var line in rewritten.Split('\n'))
            {
                if (line.Length == 0) { sb.AppendLine(); continue; }
                sb.Append(inner).AppendLine(line.TrimEnd('\r'));
            }

            if (e.ReturnType == "void")
            {
                sb.Append(inner).AppendLine($"{endLabel}: {terminator}");
            }
            else
            {
                sb.Append(inner).AppendLine($"{endLabel}: {InlinePushStatement(e.ReturnType, "__r", inlineStack)} {terminator}");
            }

            sb.Append(indent).AppendLine("}");
        }

        /// <summary>
        /// Rewrite <c>return</c> statements in a handler body to the push+goto pattern the
        /// inlined case wants.
        ///
        /// <para>For expression bodies (single <c>=&gt; expr</c>), we emit just
        /// <c>__r = &lt;expr&gt;; goto END;</c> (non-void) or <c>&lt;expr&gt;; goto END;</c> (void).</para>
        ///
        /// <para>For block bodies we preserve the original <c>{ ... }</c>, stripping the outer
        /// braces so nested locals defined in the body share the case's scope (immediates +
        /// popped operands live at the case scope, the body sees them through the same
        /// names), then apply a textual rewrite: <c>return &lt;expr&gt;;</c> becomes
        /// <c>{ __r = &lt;expr&gt;; goto END; }</c>, and bare <c>return;</c> becomes
        /// <c>goto END;</c>. Because C# <c>return</c> statements cannot appear inside
        /// sub-expressions (the keyword is statement-level), a simple non-greedy regex is
        /// safe for every handler body in the codebase (no nested lambdas or local
        /// functions in any annotated method).</para>
        /// </summary>
        private static string RewriteBody(string body, bool isExpression, string returnType, string endLabel, string pcVar, bool inlineStack)
        {
            body = RewriteCtxAccess(body, pcVar, inlineStack);
            if (isExpression)
            {
                // The body is the raw expression (no braces, no `return` keyword).
                if (returnType == "void")
                    return $"{body};";
                return $"__r = {body}; goto {endLabel};";
            }

            // Block body. Strip the outer braces (first `{` and last `}`); keep the rest.
            string inner = body.Trim();
            if (inner.StartsWith("{")) inner = inner.Substring(1);
            if (inner.EndsWith("}"))   inner = inner.Substring(0, inner.Length - 1);

            if (returnType == "void")
            {
                // Bare `return;` → `goto END;`.
                inner = Regex.Replace(inner, @"\breturn\s*;",
                                      $"goto {endLabel};");
                return inner;
            }
            else
            {
                // `return <expr>;` → `{ __r = <expr>; goto END; }`. The non-greedy match
                // stops at the first semicolon — safe because return-expressions can't
                // contain unescaped semicolons.
                inner = Regex.Replace(inner, @"\breturn\s+(.+?);",
                                      "{ __r = $1; goto " + endLabel + "; }",
                                      RegexOptions.Singleline);
                return inner;
            }
        }

        /// <summary>
        /// Rewrite hoistable ctx accesses in a handler body to their hoisted-local names.
        /// <list type="bullet">
        ///   <item><c>ctx.OpStack</c> → <c>_opStack</c></item>
        ///   <item><c>ctx.Frame.Locals.Span</c> → <c>_localsSpan</c></item>
        ///   <item><c>pc</c> → <c>{pcVar}</c> (whole-word; skipped when pcVar == "pc")</item>
        /// </list>
        /// All handlers with a <c>ref int pc</c> parameter name it <c>pc</c>, so the word-
        /// boundary rewrite reliably retargets the body at Run's local <c>_pc</c> without
        /// introducing a ref alias (which would force writes through memory, defeating the
        /// register hoist).
        ///
        /// <para>The rewrites are token-safe for today's handler bodies: none of
        /// <c>ctx.OpStack</c>, <c>ctx.Frame.Locals.Span</c>, or the identifier <c>pc</c>
        /// appears inside strings or as substrings of other identifiers. If a future
        /// handler introduces either, spell it in a form these patterns don't match
        /// (e.g. fully-qualified <c>Wacs.Core.Runtime.ExecContext.OpStack</c>, or a
        /// differently-named pc parameter).</para>
        /// </summary>
        private static string RewriteCtxAccess(string body, string pcVar, bool inlineStack)
        {
            body = body
                .Replace("ctx.OpStack", "_opStack")
                .Replace("ctx.Frame.Locals.Span", "_localsSpan");
            if (pcVar != "pc")
                body = System.Text.RegularExpressions.Regex.Replace(body, @"\bpc\b", pcVar);

            if (!inlineStack)
            {
                // Cold paths route through the Fast method calls. Same bounds-check
                // elision, but the JIT won't inline the call (Run is too big) — this
                // costs one blr per pop/push, which is acceptable in cold code and
                // keeps the cold sub-methods' frames small enough that recursive wasm
                // (call_indirect.wast) doesn't overflow the stack.
                body = body
                    .Replace("_opStack.PopI32()",   "_opStack.PopI32Fast()")
                    .Replace("_opStack.PopU32()",   "_opStack.PopU32Fast()")
                    .Replace("_opStack.PopI64()",   "_opStack.PopI64Fast()")
                    .Replace("_opStack.PopU64()",   "_opStack.PopU64Fast()")
                    .Replace("_opStack.PopF32()",   "_opStack.PopF32Fast()")
                    .Replace("_opStack.PopF64()",   "_opStack.PopF64Fast()")
                    .Replace("_opStack.PopAny()",   "_opStack.PopAnyFast()")
                    .Replace("_opStack.PushI32(",   "_opStack.PushI32Fast(")
                    .Replace("_opStack.PushU32(",   "_opStack.PushU32Fast(")
                    .Replace("_opStack.PushI64(",   "_opStack.PushI64Fast(")
                    .Replace("_opStack.PushU64(",   "_opStack.PushU64Fast(")
                    .Replace("_opStack.PushF32(",   "_opStack.PushF32Fast(")
                    .Replace("_opStack.PushF64(",   "_opStack.PushF64Fast(")
                    .Replace("_opStack.PushValue(", "_opStack.PushValueFast(");

                body = System.Text.RegularExpressions.Regex.Replace(
                    body,
                    @"_opStack\.ShiftResults\(\s*(\(int\)\w+)\s*,\s*(\(int\)\w+)\s*\);",
                    "if (_opStack.Count != $2) _opStack.ShiftResultsSlow($1, $2);");

                return body;
            }

            // Inline handler-body pop/push calls. Same reasoning as the emit-time
            // inline at the stack-param site — a `ctx.OpStack.PopI32()` or
            // `ctx.OpStack.PushI32(x)` left as a method call would land as an
            // out-of-line blr because Run is too large for the JIT to honour the
            // Fast methods' [AggressiveInlining]. The pop rewrite expands to a
            // single parenthesised expression (usable as a sub-expression in any
            // context where the original method call appeared). The push rewrite
            // expands to a self-contained `{ ... }` statement; callers emit it
            // where a statement is legal (expression-body handlers and block bodies
            // are both fine because the original call sites were statements).
            body = body
                .Replace("_opStack.PopI32()",
                         "(System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, --_opStack.Count).Data.Int32)")
                .Replace("_opStack.PopU32()",
                         "(System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, --_opStack.Count).Data.UInt32)")
                .Replace("_opStack.PopI64()",
                         "(System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, --_opStack.Count).Data.Int64)")
                .Replace("_opStack.PopU64()",
                         "(System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, --_opStack.Count).Data.UInt64)")
                .Replace("_opStack.PopF32()",
                         "(System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, --_opStack.Count).Data.Float32)")
                .Replace("_opStack.PopF64()",
                         "(System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, --_opStack.Count).Data.Float64)");

            // PopAny / PushValue / PushI32.. have an argument; do balanced-paren
            // substitution that respects nesting. Handler bodies write things like
            // `PushValue(new Value(ValType.Ref | (ValType)typeIdx, ai))` where the
            // argument spans its own parentheses — a non-greedy regex would match
            // the first `)` and get it wrong.
            body = ReplaceBalancedCall(body, "_opStack.PushI32",
                arg => "{ System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Type = Wacs.Core.Types.Defs.ValType.I32; System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Data.Int32 = (" + arg + "); _opStack.Count++; }");
            body = ReplaceBalancedCall(body, "_opStack.PushU32",
                arg => "{ System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Type = Wacs.Core.Types.Defs.ValType.I32; System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Data.UInt32 = (" + arg + "); _opStack.Count++; }");
            body = ReplaceBalancedCall(body, "_opStack.PushI64",
                arg => "{ System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Type = Wacs.Core.Types.Defs.ValType.I64; System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Data.Int64 = (" + arg + "); _opStack.Count++; }");
            body = ReplaceBalancedCall(body, "_opStack.PushU64",
                arg => "{ System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Type = Wacs.Core.Types.Defs.ValType.I64; System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Data.UInt64 = (" + arg + "); _opStack.Count++; }");
            body = ReplaceBalancedCall(body, "_opStack.PushF32",
                arg => "{ System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Type = Wacs.Core.Types.Defs.ValType.F32; System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Data.Float32 = (" + arg + "); _opStack.Count++; }");
            body = ReplaceBalancedCall(body, "_opStack.PushF64",
                arg => "{ System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Type = Wacs.Core.Types.Defs.ValType.F64; System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).Data.Float64 = (" + arg + "); _opStack.Count++; }");
            body = ReplaceBalancedCall(body, "_opStack.PushValue",
                arg => "{ System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count) = (" + arg + "); _opStack.Count++; }");

            // PopAny: same inline expansion as the generator's stack-param pop path,
            // but the body-text form is a sub-expression. Wrap in a `(expr)` that
            // evaluates to the popped Value. Uses a C# statement expression emulation
            // via an inline helper call would be cleaner but we don't have one; instead
            // produce the expanded expression that both reads the slot and nulls the
            // GcRef as an evaluation side effect.
            //
            //   (__s = Unsafe.Add(ref _registersRef, --_opStack.Count), __v = __s, __s.GcRef = null, __v)  <-- requires C# tuple-comma which doesn't exist.
            //
            // We can't model PopAny as a single expression without local mutations. So
            // rewrite `var v = _opStack.PopAny();` style via a regex: match an lvalue
            // declaration/assignment, emit the multi-line block with a scoped __s.
            body = System.Text.RegularExpressions.Regex.Replace(
                body,
                @"(\b(?:var|Wacs\.Core\.Runtime\.Value|Value)\s+(\w+)\s*=\s*)_opStack\.PopAny\(\);",
                "--_opStack.Count; Wacs.Core.Runtime.Value $2 = System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count); System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).GcRef = null;");
            // Same pattern for assignment-expressions used as statements, e.g.
            // `span[i] = _opStack.PopAny();`. Matches any simple lvalue followed by =.
            body = System.Text.RegularExpressions.Regex.Replace(
                body,
                @"([^;\n]+?)\s*=\s*_opStack\.PopAny\(\);",
                "--_opStack.Count; $1 = System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count); System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).GcRef = null;");
            // Stray _opStack.PopAny() that escaped both rewrites (shouldn't happen in
            // the current handler set, but fall back to the method call so broken
            // output is surfaced as a compile error rather than silent mis-codegen).
            body = body.Replace("_opStack.PopAny()", "_opStack.PopAnyFast()");

            // Inline the ShiftResults fast-path at emit time. The overwhelmingly common
            // branch case is one whose target's result-height equals the current stack
            // depth (no stack trimming needed) — for those, the unconditional call to
            // ShiftResults is pure overhead. Rewriting to an explicit check + call-to-
            // ShiftResultsSlow short-circuits that call without relying on the JIT to
            // inline the wrapper (which it declines for Run-sized methods). Regex captures
            // the two u32-cast arguments the emit layer produces verbatim.
            body = System.Text.RegularExpressions.Regex.Replace(
                body,
                @"_opStack\.ShiftResults\(\s*(\(int\)\w+)\s*,\s*(\(int\)\w+)\s*\);",
                "if (_opStack.Count != $2) _opStack.ShiftResultsSlow($1, $2);");

            return body;
        }

        // =====================================================================
        // Inline pop / push emission.
        // ---------------------------------------------------------------------
        // The generator emits the full body of a Fast-path pop or push at each
        // call site instead of delegating to OpStack.Pop*Fast / Push*Fast. The
        // Fast methods are [AggressiveInlining] but RyuJIT declines to inline
        // them — Run has ~500 switch cases and busts the JIT's inlining budget,
        // so every Fast call was landing as an out-of-line blr. Inlining at
        // emit time sidesteps the JIT's budget entirely and lets the JIT see
        // "ldr → .Data.Int32 → stur" as local scalar flow on a single ref.
        //
        // Layout assumptions this emit encodes:
        //   * ref Value _registersRef = ref _opStack.FirstRegister();  (hoisted in Run)
        //   * _opStack.Count is a public int field on OpStack — ++/-- work in-place
        //   * Unsafe.Add(ref T, int) is a .NET intrinsic lowering to `T* + index`
        // Push emits avoid declaring a ref local — declaring `ref Value __s` per
        // case scope bloated the Run frame enough to stack-overflow deeply
        // recursive wasm (call_indirect.wast hits ~48 InvokeWasm levels). Using
        // two `Unsafe.Add(ref _registersRef, _opStack.Count)` statements lets
        // RyuJIT CSE the address computation without adding a named slot.
        // PopV128 / PushV128 stay on the safe method calls — V128 path isn't
        // hot-loop material and its semantics (VecRef wrapping) aren't worth
        // duplicating per call site.
        // =====================================================================

        /// <summary>
        /// Emit a pop into a freshly-declared local. Returns false if the type isn't
        /// supported by the inline fast-path (e.g. V128), in which case the caller
        /// should abort the case emit with a NotSupportedException.
        /// When <paramref name="inline"/> is false, emit a method call to the
        /// OpStack's <c>Pop*Fast</c> variant instead of an inline expression —
        /// used by cold sub-methods to keep their frame small.
        /// </summary>
        private static bool EmitInlinePop(StringBuilder sb, string indent, string type, string name, bool inline)
        {
            string field = TypeToDataField(type);
            if (!inline)
            {
                string popOp = TypeToFastPop(type);
                if (popOp == null) return false;
                sb.Append(indent).AppendLine($"{type} {name} = _opStack.{popOp}();");
                return true;
            }
            if (field != null)
            {
                sb.Append(indent).AppendLine(
                    $"{type} {name} = System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, --_opStack.Count).Data.{field};");
                return true;
            }
            if (type == "Wacs.Core.Runtime.Value")
            {
                // PopAny has a GcRef-null side effect — read the slot into the named
                // local, then clear the GcRef to drop the outgoing managed reference.
                // No ref local (see push comment for why — keeps the frame narrow).
                sb.Append(indent).AppendLine($"--_opStack.Count;");
                sb.Append(indent).AppendLine(
                    $"Wacs.Core.Runtime.Value {name} = System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count);");
                sb.Append(indent).AppendLine(
                    "System.Runtime.CompilerServices.Unsafe.Add(ref _registersRef, _opStack.Count).GcRef = null;");
                return true;
            }
            if (type == "Wacs.Core.Runtime.V128")
            {
                sb.Append(indent).AppendLine($"{type} {name} = _opStack.PopV128();");
                return true;
            }
            return false;
        }

        private static string TypeToFastPop(string t) => t switch
        {
            "int"    => "PopI32Fast",
            "uint"   => "PopU32Fast",
            "long"   => "PopI64Fast",
            "ulong"  => "PopU64Fast",
            "float"  => "PopF32Fast",
            "double" => "PopF64Fast",
            "Wacs.Core.Runtime.Value" => "PopAnyFast",
            "Wacs.Core.Runtime.V128"  => "PopV128",
            _ => null,
        };

